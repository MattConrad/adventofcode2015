package main

import (
	"fmt"
	"strings"
)

type cell struct {
	x int
	y int
}

func getInitialBoard(lines []string) (int, int, map[cell]bool) {
	//if lines isn't a regular rectangle, there may be issues. but we are trusting input, here.
	x := len(lines[0])
	y := len(lines)
	board := make(map[cell]bool)
	for i, line := range lines {
		for j, char := range line {
			loc := cell{x: i, y: j}
			board[loc] = (char == 35)
		}
	}

	return x, y, board
}

func getNeighborCount(x int, y int, tox int, toy int, begin map[cell]bool) int {
	//is there a tighter way to do this?
	neighbors := 0
	for dx := -1; dx <= 1; dx++ {
		for dy := -1; dy <= 1; dy++ {
			if dx == 0 && dy == 0 {
				continue
			}
			nx := x + dx
			ny := y + dy
			if nx < 0 || nx >= tox || ny < 0 || ny >= toy {
				continue
			}
			loc := cell{x: nx, y: ny}
			if begin[loc] {
				neighbors++
			}
		}
	}

	return neighbors
}

func getIteratedBoard(tox int, toy int, begin map[cell]bool) map[cell]bool {
	final := make(map[cell]bool)
	for i := 0; i < tox; i++ {
		for j := 0; j < toy; j++ {
			loc := cell{x: i, y: j}
			neighbors := getNeighborCount(i, j, tox, toy, begin)
			//most cells after iteration will be off. start there, check exceptions.
			newState := false
			if begin[loc] && (neighbors == 2 || neighbors == 3) {
				newState = true
			}
			if !begin[loc] && neighbors == 3 {
				newState = true
			}
            //force corners to "on". drop this check to get values for part 1
            if (i == 0 || i == (tox -1)) && (j == 0 || j == (toy - 1)) {
                newState = true
            }
			final[loc] = newState
		}
	}

	return final
}

func main() {
	lines := getInput()
	tox, toy, board := getInitialBoard(lines)
	for i := 0; i < 100; i++ {
		board = getIteratedBoard(tox, toy, board)
	}

	total := 0
	for i := 0; i < tox; i++ {
		for j := 0; j < toy; j++ {
			loc := cell{x: i, y: j}
			if board[loc] {
				total++
			}
		}
	}
	fmt.Println("total cells on after 100 iterations: ", total)
}

